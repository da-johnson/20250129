// Function to Load Table
function loadTable(dataset) {
    const tableHeader = document.getElementById("table-header");
    const tableBody = document.getElementById("table-body");

    // Clear existing table
    tableHeader.innerHTML = "";
    tableBody.innerHTML = "";

    if (!datasets[dataset] || datasets[dataset].length === 0) {
        tableBody.innerHTML = "<tr><td colspan='100%'>No data available</td></tr>";
        return;
    }

    // Get Headers
    const headers = Object.keys(datasets[dataset][0]);

    // Create Header Row
    headers.forEach(header => {
        let th = document.createElement("th");
        th.textContent = header;
        tableHeader.appendChild(th);
    });

    // Create Data Rows
    datasets[dataset].forEach(row => {
        let tr = document.createElement("tr");
        headers.forEach(header => {
            let td = document.createElement("td");
            
            // If the column is "Video", create a clickable link
            if (header === "Video" && row[header]) {
                let a = document.createElement("a");
                a.href = row[header];
                a.textContent = "Video"; // Display text
                a.target = "_blank"; // Open in a new tab
                td.appendChild(a);
            } else {
                td.textContent = row[header];
            }
            tr.appendChild(td);
        });
        tableBody.appendChild(tr);
    });
}

// Function to Filter Table
function filterTable() {
    let input = document.getElementById("search").value.toLowerCase();
    let rows = document.getElementById("table-body").getElementsByTagName("tr");

    for (let row of rows) {
        let cells = row.getElementsByTagName("td");
        let found = false;
        for (let cell of cells) {
            if (cell.textContent.toLowerCase().includes(input)) {
                found = true;
                break;
            }
        }
        row.style.display = found ? "" : "none";
    }
}

